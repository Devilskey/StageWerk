namespace alerterTestLib.Object;

/// <summary>
/// colors for every loglevel.
/// </summary>
public class LoglevelLinkObject
{
    /// <summary>
    /// Gets or sets color of the trace loglevel.
    /// </summary>
    public string? Trace { get; set; }

    /// <summary>
    /// Gets or sets color of the debug loglevel.
    /// </summary>
    public string? Debug { get; set; }

    /// <summary>
    /// Gets or sets color of the information loglevel.
    /// </summary>
    public string? Information { get; set; }

    /// <summary>
    /// Gets or sets color of the warning loglevel.
    /// </summary>
    public string? Warning { get; set; }

    /// <summary>
    /// Gets or sets color of the error loglevel.
    /// </summary>
    public string? Error { get; set; }

    /// <summary>
    /// Gets or sets color of the critical loglevel.
    /// </summary>
    public string? Critical { get; set; }

    /// <summary>
    ///  Gets or sets color of the none loglevel.
    /// </summary>
    public string? None { get; set; }
}
